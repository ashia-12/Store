  //one by one item close
  function initCloseItems(elementIndex) {
    $(".onebyonecartItemsClose")
      .eq(elementIndex)
      .click(function () {
        console.log("Clicked....");
        $(this).parent().remove();
        if ($(".cartproductWrap > .cartproducts").length === 0) {
          $(".cartDefaultText").show();
          $(".subtotalCheckout").hide();
          $(".subtotalPrice").text("");
        } else {
          var quantityNum = $(this).prev().find(".cartquantity").text();
          var priceNum = $(this).prev().find(".cartprice").text();
          var subtotalpriceNum = $(".subtotalPrice").text();
          console.log(
            quantityNum + " => " + priceNum + " => " + subtotalpriceNum
          );
          var result =
            parseFloat(subtotalpriceNum) -
            parseFloat(quantityNum) * parseFloat(priceNum);
          $(".subtotalPrice").text(result.toFixed(2));
        }
      });
  }
