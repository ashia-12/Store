  function updateProducts() {
    var gender = $(".genderSelecter.selected").data("genderselect");
    var catagory = $("#catagory").val();
    var selectedProducts = products[gender][catagory];
    const $productWraper = $(".product");
    //
    selectedProducts.forEach(function (products, index) {
      var selectedColor = $productWraper.eq(index).data("selectedcolor");
      var imageName = products.photo[selectedColor];
      $productWraper.eq(index).data("selectedtype", catagory);
      $productWraper.eq(index).data("selectedgender", gender);
      $productWraper.eq(index).find("img").attr("src", imageName);
      var productName = products.name[selectedColor];
      var productPrice = products.price[selectedColor];
      $productWraper.eq(index).find(".productname").text(productName);
      $productWraper.eq(index).find(".productPrice").text(productPrice);
    });

    // selected product Catagory Title update
    var title = $("#catagory  option:selected").text();
    $(".selectedCatagoryTitle").html(title);
    //
  }
